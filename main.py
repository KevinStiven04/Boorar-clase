def sumar_dos_numeros(num1, num2) -> int:
    """
    Suma dos números y devuelve el resultado.

    Args:
        num1 (int | float): Primer número a sumar.
        num2 (int | float): Segundo número a sumar.

    Returns:
        int | float: Resultado de la suma de num1 y num2.

    """
    return num1 + num2


print(sumar_dos_numeros(1, 3))
